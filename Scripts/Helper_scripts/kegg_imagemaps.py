# Função para converter coordenadas em string para atributos de área HTML
def coords_to_area(coords):
    x1 = int(coords['x'])
    y1 = int(coords['y'])
    x2 = x1 + int(coords['width'])
    y2 = y1 + int(coords['height'])
    return f"{x1},{y1},{x2},{y2}"

